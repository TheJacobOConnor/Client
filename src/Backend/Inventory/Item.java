package Backend.Inventory;

/**
 *
 * @author jaket
 */
public class Item 
{
    protected String itemId;
    protected String itemName;
    protected String itemAcquiredDate;
    protected String itemType;
    protected String batchId;
    protected String itemStatus;
    protected String itemWeight;

    
    public Item()
    {
        
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    public String getItemId()
    {
        return itemId;
    }

    public void setItemId(String itemId)
    {
        this.itemId = itemId;
    }

    public String getItemName()
    {
        return itemName;
    }

    public void setItemName(String itemName)
    {
        this.itemName = itemName;
    }

    public String getItemAcquiredDate()
    {
        return itemAcquiredDate;
    }

    public void setItemAcquiredDate(String itemAcquiredDate)
    {
        this.itemAcquiredDate = itemAcquiredDate;
    }

    public String getItemType() 
    {
        return itemType;
    }

    public void setItemType(String itemType)
    {
        this.itemType = itemType;
    }

    public String getBatchId()
    {
        return batchId;
    }

    public void setBatchId(String batchId)
    {
        this.batchId = batchId;
    }

    public String getItemStatus()
    {
        return itemStatus;
    }

    public void setItemStatus(String itemStatus)
    {
        this.itemStatus = itemStatus;
    }

    public String getItemWeight()
    {
        return itemWeight;
    }

    public void setItemWeight(String itemWeight)
    {
        this.itemWeight = itemWeight;
    }
    
    
    
    
}
